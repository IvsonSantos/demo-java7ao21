package com.example.demojava7ao21.aula;

public class WhileTest {

    public static void main(String[] args) {

        int num = 0;

        while (num < 3) {
            System.out.println(num);
            num++;
        }

        while (true) {
            System.out.println(num);
            num++;
        }
    }
}
